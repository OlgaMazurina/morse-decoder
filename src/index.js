const MORSE_TABLE = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
  ".----": "1",
  "..---": "2",
  "...--": "3",
  "....-": "4",
  ".....": "5",
  "-....": "6",
  "--...": "7",
  "---..": "8",
  "----.": "9",
  "-----": "0",
  "*": " ",
};

function decode(expr) {
  let arr = [];
  let str = "";
  for (let i = 0; i < expr.length; i = i + 10) {
    for (let j = 0; j < 10; j++) {
      if (expr[i + j] == "1") {
        if (expr[i + j + 1] == "1") {
          str += "-";
          j++;
        } else {
          str += ".";
          j++;
        }
      } else if (expr[i + j] == "*") {
        str += "*";
        j += 10;
      }
    }
    arr.push(str);
    str = "";
  }

  for (let i = 0; i < arr.length; i++) {
    str += MORSE_TABLE[arr[i]];
  }
  return str;
}

module.exports = {
  decode,
};
